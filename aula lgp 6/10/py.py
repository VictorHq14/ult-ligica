nota = float(input("Digite uma nota de 0 a 100:  "))
if nota >= 90:
  print("parabens, sua nota foi 'A' ")
elif nota >= 80:
  print("parabens, sua nota foi 'B'")
elif nota >= 70:
  print("parabens, sua nota foi 'C'")
elif nota >= 60:
  print("parabens, sua nota foi 'D'")
else:
  print("que pena, sua nota foi 'F' ")